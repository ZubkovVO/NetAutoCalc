namespace Calc
{

    class MathematicalOperations
    {

        public static int Add(int a, int b)
        {
            var sum = a + b;
            return sum;

        }

        public static int Divide(int a, int b)
        {
            var div = a / b;
            return div;

        }

        public static int Multiply(int a, int b)
        {
            var mult = a * b;
            return mult;
        }

        public static int Substract(int a, int b)
        {
            var subs = a - b;
            return subs;
        }
    }
}